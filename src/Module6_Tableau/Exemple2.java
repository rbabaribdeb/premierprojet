package Module6_Tableau;

public class Exemple2 {
    public static void main(String[] args) {
        // tableau de char
        char [] monTableau = {'B','o','n','j','o','u','r'};
        for (int i = 0; i < monTableau.length; i++) {
            System.out.println(monTableau[i]);
        }
    }
}
